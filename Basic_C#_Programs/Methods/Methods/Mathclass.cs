using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Methods
{
    class Mathclass
    {
        public static int AddTen(int x)
        {
            return x + 10;
        }

        public static int TimesTen(int y)
        {
            return y * 10;
        }

        public static int MinusTen(int z)
        {
            return z - 10;
        }
    }
}
